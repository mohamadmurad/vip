               var form = $(this);
               var url = form.attr('action');

               $.ajax({
                   type: "POST",
                   url: url,
                   data: form.serialize(), // serializes the form's elements.
                   success: function(data)
                   {
                       $status = data['data'];
                       if ($status){
                           location.reload();
                       }else{
                           alert('العملية لم تنجح');
                       }

                   }
               });
           }




